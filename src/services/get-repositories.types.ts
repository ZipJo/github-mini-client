/** type to describe any given object inside the items[]-array from a repository-query */
export type RawGitHubRepository = {
    /** githubs UID */
    'id': number;

    /** name of the repo */
    'name': string;

    /** path and name of the repo */
    'full_name': string;

    /** amount of stars */
    'stargazers_count': number;

    /** programming language in a human-readable format */
    'language': string;

    /** link to the repo on github */
    'url': string;

    /** created at: date and time string (UTC)  */
    'created_at': string;

    /** number of forks */
    'forks': number;

    /** number of watchers */
    'watchers_count': number;

    /** author-object */
    'owner': {
        /** username */
        'login': string;

        /** link to profile */
        'url': string;

        /** ...rest  */
        [key: string]: any;
    }

    /** ...rest  */
    [key: string]: any;
}

